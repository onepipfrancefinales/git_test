<p><font color="#000000" face="Arial, Helvetica, sans-serif" size="4"><b>Chronologie</b></font></p>
<p>Le 20 mars 1892, 2 000 personnes se rassemblent sur la Pelouse de Bagatelle 
  pour assister &agrave; une finale d&#146;un championnat qui ne compte alors 
  que deux clubs. Depuis, 120 finales se sont disputées dans une vingtaine 
  de stade différents. Si certains sont aujourd&#146;hui totalement oubliés 
  (Courbevoie, Levallois), pour d&#146;autres le Brennus aura permis de forger 
  leur renommée (Stadium Municipal, Parc des Princes, Stade de France). 
  Tour d&#146;horizon de ces différents stades.<br>

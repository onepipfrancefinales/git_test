      <p><font size="5"><b>Son créateur</b></font>: <font face="Arial, Helvetica, sans-serif"><b><font size="5"> 
        Charles Brennus</font></b></font></p>
      <p><font face="Arial, Helvetica, sans-serif" size="3">Charles Brennus, pseudonyme 
        de Brennus Ambiorix Crosnier, est un médailleur, ciseleur et un 
        dirigeant sportif national fran&ccedil;ais, né &agrave; Ch&acirc;teaudun 
        le 30 novembre 1859 et mort au Mans le 23 décembre 1943.</font></p>
      <p><font face="Arial, Helvetica, sans-serif" size="3">Président de 
        la commission rugby de l&#146;USFSA<br>
        président-fondateur du SCUF</font></p>
      <p><font face="Arial, Helvetica, sans-serif" size="3">Successivement joueur, 
        arbitre, dirigeant, il exer&ccedil;ait aussi la profession de ma&icirc;tre-graveur. 
        Un tel &#147;profil&#148;, comme disent maintenant les agents recruteurs, 
        ne peut faire douter un seul instant de la paternité du trophée.</font></p>
      <p><font face="Arial, Helvetica, sans-serif" size="3">Toutefois, si Charles 
        Brennus a sans nul doute ciselé le Bouclier, le mérite de 
        la conception revient probablement &agrave; Pierre de Coubertin, Secrétaire 
        Général de l&#146;USFSA en 1892, arbitre de la 1&egrave;re 
        finale et défenseur acharné des valeurs sportives et de 
        l&#146;amateurisme de cette fin de si&egrave;cle. Précisons aussi 
        que le &#147;ciseleur&#148; s&#146;appelait de son vrai nom, Brennus Ambiorix 
        Crosnier.</font></p>

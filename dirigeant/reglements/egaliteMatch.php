<p><font face="Arial, Helvetica, sans-serif">ARTICLE 454 - MATCH NUL EN MATCH 
  &Eacute;LIMINATOIRE </font></p>
<p><font face="Arial, Helvetica, sans-serif">Les matches dits de &quot; Barrages 
  &quot; sont consid&eacute;r&eacute;s comme matches &eacute;liminatoires. </font></p>
<p><font face="Arial, Helvetica, sans-serif"><b>454-1 - Cat&eacute;gorie Seniors 
  masculins (18 ans et plus). </b><br>
  <br>
  S'il y a match nul &agrave; la fin de la dur&eacute;e r&eacute;glementaire d'un 
  match &eacute;liminatoire, l'arbitre doit, après un repos de cinq minutes, 
  prolonger la partie de vingt minutes (dix minutes de chaque c&ocirc;t&eacute;) 
  sans repos au changement de camp. <br>
  <br>
  Si, après cette prolongation, le score du match est toujours nul, il 
  ne sera pas rejou&eacute; : l'&eacute;quipe d&eacute;clar&eacute;e gagnante 
  sera celle qui aura dans l'ordre : <br>
  1- Marqu&eacute; le plus grand nombre d'essais au cours du match ; <br>
  2- R&eacute;ussi le plus grand nombre de tirs au but effectu&eacute;s dans les 
  conditions d&eacute;finies dans les règles du jeu n&deg; 9). </font></p>
<p>&nbsp;</p>
<p><font face="Arial, Helvetica, sans-serif"><b>454-3 - Matches aller et retour 
  en phases finales. </b><br>
  <br>
  Un match nul &agrave; la fin de la dur&eacute;e r&eacute;glementaire, soit du 
  match aller, soit du match retour, ne donnera pas lieu &agrave; prolongation. 
  A l'issue des deux rencontres ayant oppos&eacute; les deux &eacute;quipes en 
  matches aller et retour, l'&eacute;quipe d&eacute;clar&eacute;e gagnante sera 
  celle qui aura dans l'ordre : <br>
  <br>
  1- Obtenu le plus de points terrain (points bonus &eacute;ventuels compris) 
  &agrave; l'issue des deux rencontres, <br>
  2- Obtenu le meilleur goal-average &agrave; l'issue des deux matches, <br>
  3- Marqu&eacute; le plus grand nombre d'essais au cours des deux rencontres, 
  <br>
  4- R&eacute;ussi le plus grand nombre de tirs au but effectu&eacute;s &agrave; 
  l'issue du 2ème match dans les conditions &eacute;nonc&eacute;es dans 
  les règles du jeu (règle n&deg; 9). <br>
  </font></p>
<p>&nbsp;</p>
<p><font face="Arial, Helvetica, sans-serif"><b>ARTICLE 455 - MATCH NUL EN FINALE 
  </b><br>
  <br>
  Pour les cat&eacute;gories seniors masculins (y compris les Reichel-Espoirs) 
  des comp&eacute;titions f&eacute;d&eacute;rales, en cas d'&eacute;galit&eacute; 
  au score après les prolongations, il sera fait application des modalit&eacute;s 
  de l'article 454 ci-dessus. <br>
  <br>
  <b>Pour les f&eacute;minines</b> et pour les cat&eacute;gories masculines &quot; 
  moins de 18 ans &quot; et &quot; moins de 16 ans &quot;, en cas d'&eacute;galit&eacute; 
  au score &agrave; la fin du temps r&eacute;glementaire, <b>il n'y aura pas de 
  prolongations</b> et il sera fait application des modalit&eacute;s de l'article 
  454 ci-dessus. <br>

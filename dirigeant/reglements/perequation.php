<p> <b><font size="4" face="Arial, Helvetica, sans-serif"><br>
  1 - Inter-poules </font></b><br>
  <font face="Arial, Helvetica, sans-serif">Pour &eacute;tablir un classement 
  inter-poules en fin de phase pr&eacute;liminaire ou qualificative d'une &eacute;preuve 
  d&eacute;termin&eacute;e, il sera proc&eacute;d&eacute; &agrave; une p&eacute;r&eacute;quation 
  amenant toutes les poules au m&ecirc;me nombre de participants. </font></p>
<p><br>
  <i><font face="Arial, Helvetica, sans-serif">Exemple : Comp&eacute;tition en 
  poules de 8 associations comportant une poule de 7 associations. La p&eacute;r&eacute;quation 
  s'effectue de la fa&ccedil;on suivante : nombre total des points obtenus au 
  cours des 12 rencontres, divis&eacute; par 12 et multipli&eacute; par 14. <br>
  La p&eacute;r&eacute;quation s'effectuera de la m&ecirc;me fa&ccedil;on pour 
  le calcul du goal-average. </font></i></p>

<p><b><font size="5">Ses caract&eacute;ristiques :</font></b></p>
<p>Le Bouclier de Brennus constitue le troph&eacute;e des Champions depuis la 
  1&egrave;re finale du Championnat de France en 1892. </p>
<p>Bouclier damasquin&eacute;, il pr&eacute;sente en son centre les &#147;armes&#148; 
  de l&#146;USFSA qui - &agrave; l&#146;&eacute;poque, g&eacute;rait le rugby 
  fran&ccedil;ais - sous forme de deux anneaux enlac&eacute;s et offre comme devise 
  &#147;Ludus pro patria&#148; (&#147;le jeu pour la patrie&#148;).</p>
<p>Le Bouclier de Brennus p&egrave;se 16,850kg pour une &eacute;paisseur de 3cm, 
  une largeur de 76cm et une hauteur de 101cm.</p>
<p>Son origine est tr&egrave;s controvers&eacute;e. Beaucoup, aujourd&#146;hui 
  encore, consid&egrave;rent que son concepteur est Charles Brennus, figure embl&eacute;matique 
  du rugby fran&ccedil;ais et cr&eacute;ateur du club du SCUF.</p>
<p><font size="5"><b>Son cr&eacute;ateur</b></font>: <br>
</p>
<p>Charles Brennus</p>
<p>Successivement joueur, arbitre, dirigeant, il exer&ccedil;ait aussi la profession 
  de ma&icirc;tre-graveur. Un tel &#147;profil&#148;, comme disent maintenant 
  les agents recruteurs, ne peut faire douter un seul instant de la paternit&eacute; 
  du troph&eacute;e.</p>
<p>Toutefois, si Charles Brennus a sans nul doute cisel&eacute; le Bouclier, le 
  m&eacute;rite de la conception revient probablement &agrave; Pierre de Coubertin, 
  Secr&eacute;taire G&eacute;n&eacute;ral de l&#146;USFSA en 1892, arbitre de 
  la 1&egrave;re finale et d&eacute;fenseur acharn&eacute; des valeurs sportives 
  et de l&#146;amateurisme de cette fin de si&egrave;cle. Pr&eacute;cisons aussi 
  que le &#147;ciseleur&#148; s&#146;appelait de son vrai nom, Brennus Ambiorix 
  Crosnier.</p>
<p>Une fois encore, retenons la l&eacute;gende mais n&#146;oublions pas l&#146;histoire!</p>
<p>Ce troph&eacute;e sera donc remis en 1892 au Racing Club de France puis dispara&icirc;tra 
  (myst&egrave;re !) jusqu&#146;en 1911. </p>
<p>Remis au Stade Toulousain en 1912, il fut ignor&eacute; les deux ann&eacute;es 
  suivantes par Bayonne et le SCUF pour revenir sur le pr&eacute; en 1914 pour 
  honorer Perpignan. </p>
<p>Ce n&#146;est qu&#146;&agrave; partir de 1920 que le troph&eacute;e est confi&eacute; 
  pour une ann&eacute;e enti&egrave;re au Champion. Le premier &laquo; gardien 
  &raquo; fut le Stadoceste Tarbais. Ce n&#146;est qu&#146;&agrave; la mort de 
  Charles Brennus en 1943 que le bouclier fut remis officiellement au vainqueur 
  sur le stade &agrave; la fin de la finale. </p>
<p>Jusqu&#146;en 1951, le bouclier se trouvait sur le terrain durant la partie, 
  gard&eacute; par deux joueurs du SCUF, club de toujours de Charles Brennus. 
</p>
<p>En 1951, le capitaine de l&#146;US Carmaux, &laquo; N&eacute;nou &raquo; Pag&egrave;s, 
  vainqueur de la finale, refusa de le recevoir apr&egrave;s un combat tr&egrave;s 
  &acirc;pre face &agrave; Tarbes. </p>
<p>L&#146;ann&eacute;e suivante, le protocole fut chang&eacute;. Le bouclier restait 
  sur le terrain durant la partie mais &eacute;tait remis au capitaine des champoins 
  par le Pr&eacute;sident de la F&eacute;d&eacute;ration dans les tribunes. </p>
<p>Le premier r&eacute;cipiendaire fut Jean Prat lors de la victoire du FC Lourdes 
  en 1952. Depuis cette date, le protocole est immuable. </p>
<p>Notons que pour honorer Brennus et le club qu&#146;il cr&eacute;a, le bouclier 
  est toujours remis &agrave; l&#146;&eacute;quipe championne de France en pr&eacute;sence 
  de deux joueurs de l&#146;&eacute;cole de rugby du SCUF.</p>

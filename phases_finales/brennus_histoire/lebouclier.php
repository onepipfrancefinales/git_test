<p><b><font size="5">Ses caractéristiques :</font></b></p>
<p>Le Bouclier de Brennus constitue le trophée des Champions depuis la 
  1&egrave;re finale du Championnat de France en 1892. </p>
<p>Bouclier damasquiné, il présente en son centre les &#147;armes&#148; 
  de l&#146;USFSA qui - &agrave; l&#146;époque, gérait le rugby 
  fran&ccedil;ais - sous forme de deux anneaux enlacés et offre comme devise 
  &#147;Ludus pro patria&#148; (&#147;le jeu pour la patrie&#148;).</p>
<p>Le Bouclier de Brennus p&egrave;se 16,850kg pour une épaisseur de 3cm, 
  une largeur de 76cm et une hauteur de 101cm.</p>
<p>Son origine est tr&egrave;s controversée. Beaucoup, aujourd&#146;hui 
  encore, consid&egrave;rent que son concepteur est Charles Brennus, figure emblématique 
  du rugby fran&ccedil;ais et créateur du club du SCUF.</p>
<p><font size="5"><b>Son créateur</b></font>: <br>
</p>
<p>Charles Brennus</p>
<p>Successivement joueur, arbitre, dirigeant, il exer&ccedil;ait aussi la profession 
  de ma&icirc;tre-graveur. Un tel &#147;profil&#148;, comme disent maintenant 
  les agents recruteurs, ne peut faire douter un seul instant de la paternité 
  du trophée.</p>
<p>Toutefois, si Charles Brennus a sans nul doute ciselé le Bouclier, le 
  mérite de la conception revient probablement &agrave; Pierre de Coubertin, 
  Secrétaire Général de l&#146;USFSA en 1892, arbitre de 
  la 1&egrave;re finale et défenseur acharné des valeurs sportives 
  et de l&#146;amateurisme de cette fin de si&egrave;cle. Précisons aussi 
  que le &#147;ciseleur&#148; s&#146;appelait de son vrai nom, Brennus Ambiorix 
  Crosnier.</p>
<p>Une fois encore, retenons la légende mais n&#146;oublions pas l&#146;histoire!</p>
<p>Ce trophée sera donc remis en 1892 au Racing Club de France puis dispara&icirc;tra 
  (myst&egrave;re !) jusqu&#146;en 1911. </p>
<p>Remis au Stade Toulousain en 1912, il fut ignoré les deux années 
  suivantes par Bayonne et le SCUF pour revenir sur le pré en 1914 pour 
  honorer Perpignan. </p>
<p>Ce n&#146;est qu&#146;&agrave; partir de 1920 que le trophée est confié 
  pour une année enti&egrave;re au Champion. Le premier &laquo; gardien 
  &raquo; fut le Stadoceste Tarbais. Ce n&#146;est qu&#146;&agrave; la mort de 
  Charles Brennus en 1943 que le bouclier fut remis officiellement au vainqueur 
  sur le stade &agrave; la fin de la finale. </p>
<p>Jusqu&#146;en 1951, le bouclier se trouvait sur le terrain durant la partie, 
  gardé par deux joueurs du SCUF, club de toujours de Charles Brennus. 
</p>
<p>En 1951, le capitaine de l&#146;US Carmaux, &laquo; Nénou &raquo; Pag&egrave;s, 
  vainqueur de la finale, refusa de le recevoir apr&egrave;s un combat tr&egrave;s 
  &acirc;pre face &agrave; Tarbes. </p>
<p>L&#146;année suivante, le protocole fut changé. Le bouclier restait 
  sur le terrain durant la partie mais était remis au capitaine des champoins 
  par le Président de la Fédération dans les tribunes. </p>
<p>Le premier récipiendaire fut Jean Prat lors de la victoire du FC Lourdes 
  en 1952. Depuis cette date, le protocole est immuable. </p>
<p>Notons que pour honorer Brennus et le club qu&#146;il créa, le bouclier 
  est toujours remis &agrave; l&#146;équipe championne de France en présence 
  de deux joueurs de l&#146;école de rugby du SCUF.</p>

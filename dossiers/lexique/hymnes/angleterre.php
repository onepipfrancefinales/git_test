    <td colspan="3" class="texte"> 
      <p>&nbsp;</p>
      <p>&nbsp;</p>
      <p>&nbsp;</p>
      <p>God save our gracious Queen,<br>
        Que Dieu protège notre gracieuse Reine,<br>
     
		<span class="gras">Long live our noble Queen,</span><br>
         Longue vie &agrave; notre noble Reine,<br>
      ! 
		<span class="gras">God save the Queen!</span><br>
        Que Dieu protège la Reine !<br>
       
		<span class="gras">Send her victorious,</span><br>
        Rends-la victorieuse,<br>
     
		<span class="gras">Happy and glorious,</span><br>
         Heureuse et glorieuse ;<br>

		<span class="gras">Long to reign over us;</span><br>
              Que soit long son règne sur nous,<br>
      
		<span class="gras">God save the Queen!</span> <br>
      Que Dieu protège la Reine</p>
	  -----------------------------------------------------------
	  <p><span class="gras">O Lord, our God, arise,</span><br>
        &Ocirc; Seigneur, notre Dieu, surgis<br>
        
	   <span class="gras">Scatter her enemies,</span><br>
        Disperse ses ennemis<br>
        
		<span class="gras">And make them fall!</span><br>
        Et fais-les chuter ;<br>

		<span class="gras">Confound their politics,</span><br>
               Confonds leurs complots,<br>
       
	   <span class="gras">Frustrate their knavish tricks,</span><br>
        D&eacute;joue leurs conspirations de filou !<br>
       
	   <span class="gras">On Thee our hopes we fix,</span><br>
        En Toi, nous mettons notre espoir ;<br>
       
	   <span class="gras">God save us all!</span><br>
      Que Dieu nous protège tous !</p>
	  
	 -------------------------------------------
	 <p><span class="gras">Thy choicest gifts in store</span><br>
       Veuille bien verser sur elle<br>
       
	   <span class="gras">On her be pleased to pour;</span><br>
         Tes dons les plus pr&eacute;cieux ;<br>
        
		<span class="gras">Long may she reign;</span><br>
        Puisse-t-elle r&eacute;gner longuement ;<br>
        
		<span class="gras">May she defend our laws,</span><br>
       Puisse-t-elle d&eacute;fendre nos lois</span><br>
       
	   <span class="gras">And ever give us cause</span><br>
        Et nous donner toujours raison<br>

	   <span class="gras">To sing with heart and voice:</span><br>
                De chanter avec c&#156;ur et &agrave; pleine voix :<br>
        
		<span class="gras">God save the Queen!
		Que Dieu protège la Reine !
		</p>

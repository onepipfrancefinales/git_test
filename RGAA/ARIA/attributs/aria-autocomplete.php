                <h1>aria-autocomplete</h1>

                <h2>Définition</h2>

                L'attribut aria-autocomplete indique si la saisie de texte peut déclencher l'affichage d'une ou plusieurs prédictions de
                la valeur prévue par l'utilisateur pour un combobox, searchboxou textboxet spécifie comment les prédictions seront présentées si elles sont faites.

                Description
                La saisie semi-automatique est une fonctionnalité de l'interface utilisateur dans laquelle des suggestions en ligne sont faites
                au fur et à mesure que l'utilisateur tape une entrée. Le texte suggéré pour compléter la valeur du champ apparaît dynamiquement
                dans le champ après le curseur de saisie, et la valeur suggérée devient la valeur si l'utilisateur effectue une action, telle
                qu'une tabulation, qui fait quitter le champ.


    <h2>Définition</h2>
  L'attribut aria-hidden peut être utilisé pour masquer le contenu non interactif de l'API d'accessibilité.

L'ajout aria-hidden="true"à un élément supprime cet élément et tous ses enfants de l'arborescence d'accessibilité. Cela peut améliorer l’expérience des utilisateurs de technologies d’assistance en masquant :

Contenu purement décoratif, tel que des icônes ou des images
Contenu dupliqué, tel que du texte répété
Contenu hors écran ou réduit, tel que les menus




   
